package WhileFor;

public class Main {
    public static void main(String[] args) {

        for (int i=0;i<10;i++)
        {
            System.out.println(i);
        }
        System.out.println(" While :");

        int sayi=12;
        while (sayi>0)
        {
            System.out.println(sayi);
            sayi--;
        }

    }


}
