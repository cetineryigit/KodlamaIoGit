package SwitchDemo;

public class Main {

    public static void main(String[] args) {

        char grade = 'C';

        switch (grade) {
            case 'A':
                System.out.println("Muhteşem!");
                break;
            case 'B':
                System.out.println("Güzel not");
                break;
            case 'C':
                System.out.println("İdare eder");
                break;
            case 'D':
                System.out.println("Daha çok çalışmalısın");
                break;
            case 'F':
                System.out.println("Dersten sonra yanıma gel :)");
                break;
            default:
                System.out.println("Tekrar incelenecek");
        }

//fdsfsdf
    }
}
